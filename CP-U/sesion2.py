# TIPOS DE DintTO

# NUMERICO
int = 100
print(int)

int *= -1
print(int)


# String o Cadena
string = 'TEXTO'
print(string)

# Lista
lista = ['ELEMENTO 0', 1, -2.5, '333333', 'elemento 4']
print('Lista [valor1,valor2,...,valorN] = %s' % (lista))
print('Lista[0] = :%s' % (lista[0]))
print('Lista[2] = :%s' % (lista[2]))

# TUPLA

tupla = ('Colombia', '2022', 3333, 'Argentina', 'Chile')
print('Tupla (valor1,valor2,...,valorN) = ', tupla)
print('Tupla[4] = ', tupla[4])

# Diccionario
diccionario = {'edad': 22, 'ciudad': 'Bello', 'lista': lista, 'tupla': tupla}
print('diccionario {valor1,valor2,...,valorN} = ', diccionario)
print("diccionario['ciudad'] = ", diccionario['ciudad'])

print("diccionario['lista'] = ", diccionario['lista'])
print("diccionario['lista'][0] = ", diccionario['lista'][0])

print("diccionario['tupla'] = ", diccionario['tupla'])
print("diccionario['tupla'][3] = ", diccionario['tupla'][3])
